package JD03_Functions;

public class PT01_FunctionBasics {
    public static void main(String[] args) {
        /*
         * Functions (also called methods in Java) are reusable blocks of code
         * that perform a specific task. They help in avoiding code repetition
         * and improve program structure and readability.
         *
         * A method may:
         * - Take input values (parameters)
         * - Return a result (return value)
         *
         * Structure of a Java Method:
         *
         * 1. Method Declaration (Signature):
         *    - Defines the method's access modifier, return type, name, and parameters.
         *    - Example: public int add(int a, int b)
         *
         * 2. Method Definition:
         *    - The body of the method where logic is written inside curly braces.
         *    - Example:
         *        public int add(int a, int b) {
         *            return a + b;
         *        }
         *
         * 3. Method Call:
         *    - Executes the method with required arguments (if any).
         *    - Example: int result = add(5, 10);
         *
         * # Syntax :
         * accessSpecifier returnType methodName(parameterList) {
         *     // Method Body (method definition)
         * }
         *
         * // Example Call:
         * methodName(argumentList);
         *
         * # Method Scope
         *      Make sure that, what ever variables you define in a method, can't be access outside method
         */
    }
}
